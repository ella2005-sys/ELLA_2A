    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int rowIndex = jTable1.getSelectedRow();
    
    if (rowIndex < 0) {
        JOptionPane.showMessageDialog(null, "Please select an item!");
    } else {
        // Only open EditService if the EDIT button was clicked first
        if (isEditMode) {
            TableModel model = jTable1.getModel();
            EditService es = new EditService();

            // Pushing data from table to EditService fields
            es.jTextField1.setText(model.getValueAt(rowIndex, 0).toString()); // ID
            es.jTextField2.setText(model.getValueAt(rowIndex, 1).toString()); // Name
            es.jTextField3.setText(model.getValueAt(rowIndex, 3).toString()); // Price
            es.jComboBox1.setSelectedItem(model.getValueAt(rowIndex, 2).toString()); // Category
            es.jTextField4.setText(model.getValueAt(rowIndex, 4).toString()); // Status

            // Lock the ID field so it can't be changed
            es.jTextField1.setEditable(false);

            es.setVisible(true);
            es.setLocationRelativeTo(null);
            this.dispose(); // Close management screen
            
            isEditMode = false; // Reset mode for next time
        }
    }
    }//GEN-LAST:event_jTable1MouseClicked

