public void loadServices() {
    try {
        String sql;
        // Check if current user is a provider
        if (CONFIG.Session.getRole().equalsIgnoreCase("PROVIDER")) {
            // Only load services for the logged-in provider
            sql = "SELECT * FROM tbl_services WHERE provider_id = " + CONFIG.Session.getUserId();
        } else {
            // Admin sees all services
            sql = "SELECT * FROM tbl_services";
        }
        cfg.displayData(sql, jTable1);
    } catch (Exception e) {
        System.out.println("CRITICAL ERROR: " + e.getMessage());
    }
}

   
private void searchService() {
    String query = jTextFieldSearch.getText().trim();
    String sql;

    if (CONFIG.Session.getRole().equalsIgnoreCase("PROVIDER")) {
        // Provider search only within their own services
        sql = "SELECT * FROM tbl_services WHERE provider_id = " + CONFIG.Session.getUserId()
            + " AND (s_name LIKE '%" + query + "%' OR s_category LIKE '%" + query + "%')";
    } else {
        // Admin can search all services
        sql = "SELECT * FROM tbl_services WHERE s_name LIKE '%" + query + "%' OR s_category LIKE '%" + query + "%'";
    }

    cfg.displayData(sql, jTable1);
}

